jQuery.noConflict();
//initialize header to faded..
jQuery(document).ready(function(){
										
		jQuery("article").each(function(){
												
			jQuery("article").fadeTo(300, 0.55);
			
			jQuery(this).hover(
				function(){
					jQuery(this).fadeTo(300, 1.0);
				}, 
		
				function(){
					jQuery(this).fadeTo(300, 0.55);
				});	
		});
});
		
		
		
		
	
